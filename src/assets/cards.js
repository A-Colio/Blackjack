function createDeck() {
  const deck = [];
  deck.splice(0, 311);
  for (let i = 0; i < 6; i++) {
    deck.push(...Object.values(cards));
  }
  for (let i = deck.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [deck[i], deck[j]] = [deck[j], deck[i]];
  }
  return deck;
}

